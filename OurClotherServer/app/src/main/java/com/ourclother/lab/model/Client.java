public class Client {
    private int number;
    private char name;
    private int age;
    private int weight;

    public char getName() {
        return name;
    }

    public void setName(char name) {
        this.name = name;
    }



    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
